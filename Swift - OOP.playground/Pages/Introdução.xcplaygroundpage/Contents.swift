//: ## Swift como uma linguagem multi-paradigma
//:

//: Paradigma de programação é uma maneira de
//: classificar as linguagens de programação de acordo
//: com a maneira como elas estruturam a resolução
//: de um problema. Entre os paradigmas de programação
//: podemos citar linguagens procedurais, orientadas
//: a objetos, funcionais, lógicas etc.
//:
//: Swift é uma linguagem multi-paradigma, ou seja,
//: suporta mais de um paradigma de programação ou
//: mais de uma "forma de pensar" para a resolução
//: de problemas.
//:
//: Neste *playground* veremos como utilizar as
//: estruturas básicas para definir os tipos abstratos
//: de dados associados ao paradigma de orientação a
//: objetos.

